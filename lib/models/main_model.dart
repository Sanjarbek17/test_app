class Product {
  final int id;
  final String name;
  final String code;
  final double distance;
  final String address;
  final int time;
  final String storeType;

  Product({
    required this.id,
    required this.name,
    required this.code,
    required this.distance,
    required this.address,
    required this.time,
    required this.storeType,
  });
}
